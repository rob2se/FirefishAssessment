package enums;

/**
 * Enum representing the different types of browsers that can be used for testing.
 * This enum provides a central place for managing browser types supported by the application.
 * Author: Rob Thomson
 * Date: 06/06/2024
 */
public enum Browsers {
    /**
     * Google Chrome browser.
     */
    Chrome,

    /**
     * Mozilla Firefox browser.
     */
    Firefox,

    /**
     * Internet Explorer browser.
     */
    IE,

    /**
     * BrowserStack for cross-browser testing in the cloud.
     */
    BrowserStack
}
